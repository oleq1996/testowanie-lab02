package com.company;

class Person {

    private String firstName;
    private String lastName;
    private int age;
    private String email;
    private Address address;


    public Person(String firstName,
                  String lastName,
                  int age,
                  String email,
                  Address address) {

        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.email = email;
        this.address = address;
    }




}